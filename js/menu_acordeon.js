$(function() {
    var menusnippets = function(el, multiple) {
        this.el = el || {};
        this.multiple = multiple || false;
 
        // Variables privadas
        var links = this.el.find('.opcion');
        // Evento
        links.on('click', {el: this.el, multiple: this.multiple}, this.dropdown)
    }
 
    menusnippets.prototype.dropdown = function(e) {
        var $el = e.data.el;
            $this = $(this),
            $next = $this.next();
 
        $next.slideToggle();
        $this.parent().toggleClass('open');
 
        if (!e.data.multiple) {
            $el.find('.subopcion').not($next).slideUp().parent().removeClass('open');
        };
    }   
 
    var menusnippets = new menusnippets($('#menusnippets'), false);
});
